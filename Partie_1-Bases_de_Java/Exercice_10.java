//10. Vérifier si un nombre est positif, négatif ou nul.
public class Exercice_10 {
    public static void main(String[] args) {
        int x = 0;
        if (x > 0) {
            System.out.println("Positif");
        }
        else if (x == 0) {
            System.out.println("Nul");
        }
        else {
            System.out.println("Négatif");
        }

    }
}
