//4. Effectuer des opérations arithmétiques de base (+, -, \*, /, %).
public class Exercice_4 {
    public static void main(String[] args) {
    int add = 5+2;
    int sous = 7-5;
    int mult = 2*7;
    float div = 14/9;
    System.out.println("5 + 2 = " + add);    
    System.out.println("7 - 5 = " + sous);  
    System.out.println("2 x 7 = " + mult);  
    System.out.println("14 / 9 = " + div);
    }
}
